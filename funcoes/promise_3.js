function gerarNumerosEntre(min, max) {
    if(min > max) {
        //destructuring à esquerda, array à direita
        [min, max] = [max, min]

    }
    return new Promise(resolve => {
        const fator = (max - min + 1)
        const aleatorio = parseInt(Math.random() * fator) + min
        resolve(aleatorio)
    })
}

gerarNumerosEntre(1, 60).then(console.log)

gerarNumerosEntre(1, 50).then(num => num * 10)
                        .then(numX10 => `O número gerado vezes 10 foi ${numX10} `)
                        .then(console.log)